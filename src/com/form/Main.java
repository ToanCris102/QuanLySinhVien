    private boolean checkInputData() {
        String ex = "B\\d{7}";
        if (!txtMSSV.getText().matches(ex)) {
            JOptionPane.showMessageDialog(rootPane, "Nhap sai, vi du dung B1805927");
            txtMSSV.setText("");
            return false;
        }
//        ex = "^[a-zA-Z]";
//        if(!txtTenSV.getText().matches(ex)){
//            JOptionPane.showMessageDialog(rootPane, "Nhap sai, vi du Tên không được nhập số");
//            txtTenSV.setText("");
//        }
//        ex = "^[a-zA-Z]";
//        if(!txtHoSV.getText().matches(ex)){
//            JOptionPane.showMessageDialog(rootPane, "Nhap sai, vi du Tên không được nhập số");
//            txtHoSV.setText("");
//        }
        ex = "\\d{2}-\\d{2}-\\d{4}";
        if (!txtNgaySinh.getText().matches(ex)) {
            JOptionPane.showMessageDialog(rootPane, "Nhap sai, vi du dung 00-00-0000");
            txtNgaySinh.setText("");
            return false;
        }
        if (!txtSoDienThoai.getText().isEmpty()) {
            ex = "\\d{10}";
            if (!txtSoDienThoai.getText().matches(ex)) {
                JOptionPane.showMessageDialog(rootPane, "Nhap sai, số điện thoại phải là 10 số");
                txtSoDienThoai.setText("");
                return false;
            }
        }
        return true;
    }

