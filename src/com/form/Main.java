    private void setTable() {
        List<ModelTable> list = convider.getDB();
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        for (ModelTable row : list) {
            Vector vector = new Vector();
            vector.add(row.getId());
            vector.add(row.getHoSV());
            vector.add(row.getTenSV());
            vector.add(row.getMssv());
            vector.add(row.getKhoa());
            vector.add(row.getMaNganh());
            vector.add(row.getGioiTinh());
            vector.add(row.getNgaySinh());
            vector.add(row.getSoDienThoai());
            tableModel.addRow(vector);
        }
    }

