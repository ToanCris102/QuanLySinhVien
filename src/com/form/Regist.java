    private void reset() {
        txtFullName.setText("");
        txtUserName.setText("");
        txtConPass.setText("");
        txtPassword.setText("");
        cboPermission.setSelectedItem("User");
    }
    
    private boolean checkEmpty(){
        if(txtFullName.getText().isEmpty() || txtUserName.getText().isEmpty() || txtPassword.getText().isEmpty() || txtConPass.getText().isEmpty())
            return true;
        return false;
    }
    
    
