    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        ModelAdmin modelAd = new ModelAdmin();
        ModelDecentralization modelDe = new ModelDecentralization();
        int admin_id = conviderAd.getAdmin_id()+1;
        modelAd.setAdmin_id(admin_id);
        modelAd.setUserName(txtUserName.getText());
        modelAd.setPassWord(txtConPass.getText());
        modelAd.setFullName(txtFullName.getText());
        modelDe.setAdmin_id(admin_id);
        modelDe.setQuyen_id(cboPermission.getSelectedItem().equals("Admin") ? 1:2);
        if(checkEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Phải nhập dữ liệu đầy đủ");
            return;
        }
        if(!txtPassword.getText().equals(txtConPass.getText())){
            JOptionPane.showMessageDialog(rootPane, "Password nhập lại không đúng");
            return;
        }
            
        conviderAd.create(modelAd, modelDe);
        reset();
    }//GEN-LAST:event_btnCreateActionPerformed

